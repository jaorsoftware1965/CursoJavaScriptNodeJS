// Operaciones con los operadores lógicos:
// AND &&, OR|| y NOT !
// ----------------------------------------------------------------------

// Mensaje
console.log('05 Operadores Logicos');

// Variables booleanas
var a = true;
var b = false;

// Operaciones lógicas
console.log("(a && b) => ",a && b);
console.log("(a || b) => ",a || b);
console.log("!(a && b) => ",!(a && b));
console.log("")

// Tablas de verdad AND
console.log("Tabla de Verdad AND");
console.log("true  && true  => ",true && true);
console.log("true  && false => ",true && false);
console.log("false && true  => ",false && true);
console.log("false && false => ",false && false);
console.log("")

// Tablas de verdad OR
console.log("Tabla de Verdad OR");
console.log("true  || true  => ",true || true);
console.log("true  || false => ",true || false);
console.log("false || true  => ",false || true);
console.log("false || false => ",false || false);
console.log("")

// Tablas de verdad NOT
console.log("Tabla de Verdad NOT");
console.log("! true  => ",!true);
console.log("! false => ",!false);
console.log("")



